#include "../../includes/exec.h"
#include "../../includes/minishell.h"

void    restore_all_in_out(int saved_stdin, int saved_stdout)
{
    dup2(saved_stdin, STDIN_FILENO);
    dup2(saved_stdout, STDOUT_FILENO);
    close(saved_stdin);
    close(saved_stdout);
}